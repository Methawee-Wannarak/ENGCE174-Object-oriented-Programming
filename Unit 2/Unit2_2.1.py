# รับค่าจากผู้ใช้
rows = int(input("กรุณากรอกจำนวนแถว: "))
 
# ตรวจสอบความถูกต้องของค่าที่กรอก
if rows < 1 :
    print("กรุณากรอกจำนวนแถวระหว่าง 1")
else:
    # แสดงรูปแบบดาวจากน้อยไปมาก
    print("\nรูปแบบดาวจากน้อยไปมาก:")
    for i in range(1, rows + 1):
        print('*' * i)
 
    print("\n---------------------------------------")
 
    # แสดงรูปแบบดาวจากมากไปน้อย
    print("รูปแบบดาวจากมากไปน้อย:")
    for i in range(rows, 0, -1):
        print('*' * i)
 
    print("\n---------------------------------------")
 
    # ฟังก์ชันเพื่อพิมพ์รูปแบบดาวที่จัดกลาง
    def print_centered_pattern(rows):
        for i in range(1, rows + 1):
            print(' ' * (rows - i) + '* ' * i)
 
    print("รูปแบบดาวที่จัดกลาง:")
    print_centered_pattern(rows)
 
    print("\n---------------------------------------")
 
    # ฟังก์ชันเพื่อพิมพ์รูปแบบดาวที่จัดกลางกลับด้าน
    def print_reversed_centered_pattern(rows):
        for i in range(rows, 0, -1):
            print(' ' * (rows - i) + '* ' * i)
 
    print("รูปแบบดาวที่จัดกลางกลับด้าน:")
    print_reversed_centered_pattern(rows)
   
    print("\n---------------------------------------")
 
    # แสดงรูปแบบดาวจากน้อยไปมาก แต่ไม่มีช่องว่างระหว่างดาว
    print("รูปแบบดาวจากน้อยไปมาก (ไม่มีช่องว่างระหว่างดาว):")
    for i in range(1, rows + 1):
        print(' ' * (rows - i) + '*' * i)